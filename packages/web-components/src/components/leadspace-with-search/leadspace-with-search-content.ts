import { property } from 'lit/decorators.js';
import settings from '@carbon/ibmdotcom-utilities/es/utilities/settings/settings.js';
import StableSelectorMixin from '../../globals/mixins/stable-selector';
import C4DLeadspaceBlockContent from '../leadspace-block/leadspace-block-content';
import styles from './leadspace-with-search.scss';
import { carbonElement as customElement } from '../../internal/vendor/@carbon/web-components/globals/decorators/carbon-element';

const { stablePrefix: c4dPrefix } = settings;

/**
 * Content in Leadspace with search.
 *
 * @element c4d-leadspace-block-content
 */
@customElement(`${c4dPrefix}-leadspace-with-search-content`)
class C4DLeadspaceWithSearchContent extends StableSelectorMixin(
  C4DLeadspaceBlockContent
) {
  /**
   * The shadow slot the content should be in.
   */
  @property({ reflect: true })
  slot = 'content';

  static get stableSelector() {
    return `${c4dPrefix}--leadspace-with-search__content`;
  }

  static styles = styles; // `styles` here is a `CSSResult` generated by custom WebPack loader
}

/* @__GENERATE_REACT_CUSTOM_ELEMENT_TYPE__ */
export default C4DLeadspaceWithSearchContent;
