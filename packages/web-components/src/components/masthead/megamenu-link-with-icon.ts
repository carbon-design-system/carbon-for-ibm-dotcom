import { css } from 'lit';
import { property } from 'lit/decorators.js';
import CDSLink from '../../internal/vendor/@carbon/web-components/components/link/link.js';
import settings from '../../internal/vendor/@carbon/ibmdotcom-utilities/utilities/settings/settings';
import { MEGAMENU_LINK_WITH_ICON_STYLE_SCHEME } from './defs';
import styles from './masthead.scss';
import { carbonElement as customElement } from '../../internal/vendor/@carbon/web-components/globals/decorators/carbon-element.js';

export { MEGAMENU_LINK_WITH_ICON_STYLE_SCHEME };

const { stablePrefix: ddsPrefix } = settings;

/**
 * Megamenu link with icon.
 *
 * @element dds-megamenu-link-with-icon
 */
@customElement(`${ddsPrefix}-megamenu-link-with-icon`)
class DDSMegaMenuLinkWithIcon extends CDSLink {
  /**
   * link title.
   */
  @property({ reflect: true, attribute: 'style-scheme' })
  styleScheme = MEGAMENU_LINK_WITH_ICON_STYLE_SCHEME.DEFAULT;

  // `styles` here is a `CSSResult` generated by custom WebPack loader
  static get styles() {
    return css`
      ${super.styles}${styles}
    `;
  }
}

export default DDSMegaMenuLinkWithIcon;
