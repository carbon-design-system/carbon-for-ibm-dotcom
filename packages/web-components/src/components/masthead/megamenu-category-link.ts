import { html, property, customElement } from 'lit-element';
import ddsSettings from '@carbon/ibmdotcom-utilities/es/utilities/settings/settings.js';
import DDSLink from '../../globals/internal/link';
import styles from './masthead.scss';

const { stablePrefix: ddsPrefix } = ddsSettings;

/**
 * MegaMenu
 *
 * @element dds-megamenu
 * @slot brand - The left hand area.
 * @slot nav - The nav content.
 * @slot profile - The right hand area.
 */
@customElement(`${ddsPrefix}-megamenu-category-link`)
class DDSMegaMenuCateoryLink extends DDSLink {
  /**
   * link title.
   */
  @property({ reflect: true })
  title = '';

  /**
   * autoid.
   */
  @property({ reflect: true })
  autoid = '';

  /**
   * index for data-autoid.
   */
  @property({ reflect: true })
  index = 0;

  connectedCallback() {
    if (!this.hasAttribute('tabindex')) {
      this.setAttribute('tabindex', '0');
    }
    if (!this.hasAttribute('data-autoid')) {
      const { autoid, index } = this;
      this.setAttribute('data-autoid', `${autoid}-item${index}`);
    }
    super.connectedCallback();
  }

  /**
   * @returns The inner content.
   */
  protected _renderInner() {
    const { title } = this;
    return html`
      <span>${title}</pan>
    `;
  }

  static styles = styles; // `styles` here is a `CSSResult` generated by custom WebPack loader
}

export default DDSMegaMenuCateoryLink;
