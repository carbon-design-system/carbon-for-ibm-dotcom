import { css } from 'lit';
import { property } from 'lit/decorators.js';
import settings from '@carbon/ibmdotcom-utilities/es/utilities/settings/settings.js';
import C4DLinkWithIcon from '../link-with-icon/link-with-icon';
import styles from './link-list.scss?lit';

import { LINK_LIST_ITEM_TYPE } from './defs';
import { carbonElement as customElement } from '@carbon/web-components/es/globals/decorators/carbon-element.js';

const { stablePrefix: c4dPrefix } = settings;

/**
 * Link list item.
 *
 * @element c4d-link-list-item
 */
@customElement(`${c4dPrefix}-link-list-item`)
class C4DLinkListItem extends C4DLinkWithIcon {
  /**
   * Defines the style of the link-list-item: `default` or `end`
   */
  @property({ reflect: true })
  type = LINK_LIST_ITEM_TYPE.DEFAULT;

  /**
   * Positions the icon inline with text when `true`
   */
  @property({ type: Boolean })
  iconInline = false;

  connectedCallback() {
    if (!this.hasAttribute('role')) {
      this.setAttribute('role', 'listitem');
    }
    super.connectedCallback();
  }

  static get stableSelector() {
    return `${c4dPrefix}--link-list-item`;
  }

  // `styles` here is a `CSSResult` generated by custom WebPack loader
  static get styles() {
    return css`
      ${super.styles}${styles}
    `;
  }
}

/* @__GENERATE_REACT_CUSTOM_ELEMENT_TYPE__ */
export default C4DLinkListItem;
