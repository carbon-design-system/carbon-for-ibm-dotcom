import { css, customElement, html } from 'lit-element';
import ddsSettings from '@carbon/ibmdotcom-utilities/es/utilities/settings/settings.js';
import sameHeight from '@carbon/ibmdotcom-utilities/es/utilities/sameHeight/sameHeight';
import DDSContentItem from '../content-item/content-item';
import styles from './content-block-headlines.scss';

const { stablePrefix: ddsPrefix } = ddsSettings;

/**
 * Content Block Headlines item
 *
 * @element dds-content-block-headlines-item
 */
@customElement(`${ddsPrefix}-content-block-headlines-item`)
class DDSContentBlockHeadlinesItem extends DDSContentItem {
  private itemCopy;

  render() {
    return html`
      <h4 class="bx--content-block-headlines__headline"><slot name="headline"></slot></h4>
      <p class="bx--content-block-headlines__copy"><slot @slotchange="${this._handleSlotChange}" name="copy"></slot></p>
    `;
  }

  connectedCallback() {
    super.connectedCallback();
    window.addEventListener('resize', this._handleResize);
  }

  disconnectedCallback() {
    super.disconnectedCallback();
    window.removeEventListener('resize', this._handleResize);
  }

  /**
   * Method called on resize, triggers the sameHeight utility
   *
   * @private
   */
  private _handleResize() {
    if (this.itemCopy) window.requestAnimationFrame(() => sameHeight(this.itemCopy, 'md'));
  }

  /**
   * Handler for @slotChange, toggles the split layout class and set the children link-list-item to the same height
   *
   * @private
   */
  private _handleSlotChange(event: Event) {
    this.itemCopy = (event.target as HTMLSlotElement)
      .assignedNodes({ flatten: true })
      .filter(
        elem => (elem as HTMLElement).localName === (this.constructor as typeof DDSContentBlockHeadlinesItem).itemCopySelector
      );
    sameHeight(this.itemCopy, 'md');
  }

  static get itemCopySelector() {
    return `${ddsPrefix}-content-item-copy`;
  }

  // `styles` here is a `CSSResult` generated by custom WebPack loader
  static get styles() {
    return css`${super.styles}${styles}`;
  }
}

export default DDSContentBlockHeadlinesItem;
