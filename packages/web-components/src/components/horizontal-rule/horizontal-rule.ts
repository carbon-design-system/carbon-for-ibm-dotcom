import { html, property, customElement, LitElement } from 'lit-element';
// import ifNonNull from 'carbon-web-components/es/globals/directives/if-non-null';
import settings from 'carbon-components/es/globals/js/settings';
import ddsSettings from '@carbon/ibmdotcom-utilities/es/utilities/settings/settings';
import styles from './horizontal-rule.scss';

const { prefix } = settings;
const { stablePrefix: ddsPrefix } = ddsSettings;

/**
 * Horizontal Rule
 *
 * @element dds-hr
 */
@customElement(`${ddsPrefix}-hr`)
class DDSHorizontalRule extends LitElement {
  /**
   * Style of the HR (solid (default) | dashed)
   */
  @property({ attribute: 'type' })
  type = 'solid';

  /**
   * Length of the HR
   * (fluid (default) | small | medium | large)
   */
  @property({ attribute: 'size' })
  size = 'fluid';

  /**
   * Contrast of the HR
   * (medium-contrast (default) | low-contrast | high-contrast)
   */
  @property({ attribute: 'contrast' })
  contrast = 'medium-contrast';

  /**
   * Weight of the HR
   * (thin (default) | thick)
   */
  @property({ attribute: 'weight' })
  weight = 'thin';

  render() {
    const { type, size, contrast, weight } = this;
    return html`
      <hr
        class="${prefix}--hr ${prefix}--hr--${type} ${prefix}--hr--${size} ${prefix}--hr--${contrast} ${prefix}--hr--${weight}"
      />
    `;
  }

  static get stableSelector() {
    return `${ddsPrefix}--hr`;
  }

  static styles = styles; // `styles` here is a `CSSResult` generated by custom WebPack loader
}

export default DDSHorizontalRule;
