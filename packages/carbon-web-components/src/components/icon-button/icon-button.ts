import { html } from 'lit';
import { property, customElement } from 'lit/decorators.js';
import { prefix } from '../../globals/settings';
import '../tooltip/index';
import '../button/index';
import CDSButton from '../button/button';
import { ICON_BUTTON_SIZE } from './defs';
import styles from './icon-button.scss';

export { ICON_BUTTON_SIZE };

/**
 * Icon Button
 *
 */
@customElement(`${prefix}-icon-button`)
class CDSIconButton extends CDSButton {
  @property({ reflect: true, type: String })
  align = 'top';

  @property({ reflect: true, type: Boolean })
  defaultOpen = false;

  @property({ attribute: 'enter-delay-ms', type: Number })
  enterDelayMs = 100;

  @property({ attribute: 'exit-delay-ms', type: Number })
  exitDelayMs = 300;

  /**
   * Button size.
   */
  @property({ reflect: true })
  size = 'md';

  updated() {
    this.shadowRoot
      ?.querySelector(`${prefix}-tooltip`)
      ?.shadowRoot?.querySelector(`.${prefix}--tooltip`)
      ?.classList.add(`${prefix}--icon-tooltip`);
  }

  render() {
    const { align, defaultOpen, enterDelayMs, exitDelayMs } = this;
    return html`
      <cds-tooltip
        align=${align}
        ?defaultOpen=${defaultOpen}
        closeOnActivation=${true}
        enterDelayMs=${enterDelayMs}
        exitDelayMs=${exitDelayMs}>
        ${super.render()}
        <cds-tooltip-content>
          <slot name="tooltip-content"></slot>
        </cds-tooltip-content>
      </cds-tooltip>
    `;
  }

  static styles = styles; // `styles` here is a `CSSResult` generated by custom WebPack loader
}

export default CDSIconButton;
